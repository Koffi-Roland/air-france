package com.afklm.rigui.fonetik;

public class TabSubstStr {
	private String texte;
	private String phone;
	
	public TabSubstStr(String texte, String phone) {
		this.texte = texte;
		this.phone = phone;
	}

	public String getTexte() {
		return texte;
	}

	public String getPhone() {
		return phone;
	}
}
