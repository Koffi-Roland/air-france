package com.afklm.rigui.model.individual;

public class ModelLettreCompte {
	
	private Integer icle;
    private String sgin;
    private String slettreComptoir;
    
	public Integer getIcle() {
		return icle;
	}
	public void setIcle(Integer icle) {
		this.icle = icle;
	}
	public String getSgin() {
		return sgin;
	}
	public void setSgin(String sgin) {
		this.sgin = sgin;
	}
	public String getSlettreComptoir() {
		return slettreComptoir;
	}
	public void setSlettreComptoir(String slettreComptoir) {
		this.slettreComptoir = slettreComptoir;
	}

}
